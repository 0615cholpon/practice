package Inputs;

public class Increments {

	public static void main(String[] args) {
		int size =50;
		System.out.println(++size);
		System.out.println(size);
		System.out.println(size++);
		System.out.println(size);
		
		int age =40;
		int otherAge;
		otherAge = age;
		System.out.println(otherAge);
		System.out.println(age);
		
		
		
		
		
	}
	
	
}
